package jp.terasoluna.fw.collector.concurrent;

import java.util.concurrent.BlockingQueue;

/**
 * キューイングの終了を通知するBlockingQueueインタフェース
 */
public interface NotificationBlockingQueue<E> extends BlockingQueue<E> {
    /**
     * キューイングの終了を通知する。
     * <p>
     * キューに要素が入るのを待っているスレッドがいる場合、そのブロックを解除する。 キューに要素を詰めるスレッドは、キューイングが完了したあとで、必ずこのメソッドを実行すること。
     * </p>
     */
    void finishQueueing();
}
